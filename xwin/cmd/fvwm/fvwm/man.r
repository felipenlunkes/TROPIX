.in 5
O gerenciador de janelas "fvwm" é baseado no cliente "fvwm2", distribuído
como programa de contribuição ao sistema gráfico X-Window.
Sua função é capturar informações sobre cada janela
criada na tela gráfica, contornando-as com bonitas bordas, permitindo
que elas sejam movimentadas, destruídas, minimizadas, controlando o foco
e organizando a tela gráfica em "desktops" independentes.

.sp
O "fvwm" não é executado diretamente por iniciativa do usuário,
porém pelo programa "xinit" (veja "xinit (xwin)").

.sp
O "fvwm" pode ter seu funcionamento modificado através de opções
integrantes do arquivo $HOME/.fvwmrc. Para maiores detalhes
veja "fvwmrc (fmt)".

